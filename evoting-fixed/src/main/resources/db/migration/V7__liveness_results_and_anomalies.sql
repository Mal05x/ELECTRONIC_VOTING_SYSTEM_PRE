-- V7: Liveness results table for server-side CAM stream evaluation
-- Architecture change: ESP32-CAM no longer evaluates locally; streams JPEG
-- to POST /api/camera/liveness, result stored here keyed by session_id.

CREATE TABLE IF NOT EXISTS liveness_results (
    id               BIGSERIAL PRIMARY KEY,
    session_id       VARCHAR(64)  NOT NULL UNIQUE,
    liveness_passed  BOOLEAN      NOT NULL DEFAULT FALSE,
    frame_bytes      INTEGER,
    terminal_id      VARCHAR(64),
    evaluated_at     TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
    consumed         BOOLEAN      NOT NULL DEFAULT FALSE
);

CREATE INDEX IF NOT EXISTS idx_liveness_session_id
    ON liveness_results(session_id);

CREATE INDEX IF NOT EXISTS idx_liveness_evaluated_at
    ON liveness_results(evaluated_at DESC);

-- Comments for documentation
COMMENT ON TABLE  liveness_results IS
    'Server-side liveness evaluation results from ESP32-CAM JPEG streams. '
    'Records expire after 10 minutes via scheduled cleanup in BiometricService.';

COMMENT ON COLUMN liveness_results.session_id IS
    'Shared ID generated by ESP32-S3 before triggering CAM. '
    'AuthenticationService queries this table using the same ID.';

COMMENT ON COLUMN liveness_results.consumed IS
    'TRUE after AuthenticationService reads this result. '
    'Prevents session replay: each liveness result can only be used once.';
